public class UserMobileVO {
	private String clientType ;
	private String clientBrand ;
	private String clientModel ;
	private String clientSysNo ;
	private String clientDevNo ;
	public String getClientType() {
		return clientType;
	}
	public void setClientType(String clientType) {
		this.clientType = clientType;
	}
	public String getClientBrand() {
		return clientBrand;
	}
	public void setClientBrand(String clientBrand) {
		this.clientBrand = clientBrand;
	}
	public String getClientModel() {
		return clientModel;
	}
	public void setClientModel(String clientModel) {
		this.clientModel = clientModel;
	}
	public String getClientSysNo() {
		return clientSysNo;
	}
	public void setClientSysNo(String clientSysNo) {
		this.clientSysNo = clientSysNo;
	}
	public String getClientDevNo() {
		return clientDevNo;
	}
	public void setClientDevNo(String clientDevNo) {
		this.clientDevNo = clientDevNo;
	}
	

}
